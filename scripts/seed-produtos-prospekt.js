async function main() {
  console.log(`\n=== Seed de Produtos PROSPEKT ===`);
  console.log(`Total de produtos na lista: ${PRODUTOS.length}`);

  // Busca produtos existentes para evitar duplicidade
  const { data: existentes } = await sb.from('produtos').select('nome').eq('ativo', true);
  const nomesExistentes = new Set((existentes || []).map(p => p.nome.trim().toUpperCase()));

  let inseridos = 0;
  let ignorados = 0;

  for (const nome of PRODUTOS) {
    const nomeUpper = nome.trim().toUpperCase();
    if (nomesExistentes.has(nomeUpper)) {
      ignorados++;
      continue;
    }
    const id = crypto.randomBytes(16).toString('hex');
    const { error } = await sb.from('produtos').insert({
      id,
      nome: nome.trim(),
      descricao: null,
      preco: null,
      cor: '#6CFF4E',
      ativo: true,
      criado_em: new Date().toISOString(),
    });
    if (error) {
      console.error(`❌ Erro ao inserir "${nome}":`, error.message);
    } else {
      inseridos++;
    }
  }

  console.log(`\n✅ Produtos inseridos: ${inseridos}`);
  console.log(`⏩ Já existiam (ignorados): ${ignorados}`);
  console.log(`📦 Total na lista: ${PRODUTOS.length}\n`);
}

main().catch(e => { console.error(e.message); process.exit(1); });
